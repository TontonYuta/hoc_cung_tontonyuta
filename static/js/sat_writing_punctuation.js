const snippets_sat_writing_punctuation = `
<h3>✍️ SAT Writing – Punctuation</h3>

<p><b>1. Comma (,):</b></p>
<ul>
  <li>Tách các thành phần trong danh sách: <i>apples, oranges, and bananas</i>.</li>
  <li>Sau mệnh đề phụ đứng trước: <i>When it rains, we stay inside.</i></li>
  <li>Trước liên từ nối 2 mệnh đề độc lập: <i>I was late, but she waited.</i></li>
</ul>

<p><b>2. Semicolon (;):</b></p>
<ul>
  <li>Nối 2 mệnh đề độc lập có liên hệ chặt chẽ: <i>I have a test tomorrow; I need to study tonight.</i></li>
  <li>Dùng trong danh sách phức tạp có dấu phẩy: <i>Paris, France; Rome, Italy; and Madrid, Spain.</i></li>
</ul>

<p><b>3. Colon (:):</b></p>
<ul>
  <li>Dùng để giới thiệu danh sách, giải thích: <i>He had one goal: to win.</i></li>
</ul>

<p><b>4. Dash (—):</b></p>
<ul>
  <li>Nhấn mạnh hoặc thêm thông tin: <i>He finally arrived — two hours late.</i></li>
</ul>
`;


